import itertools

class ConstCompressionCode:
	def __init__(self, keycodes, length):
		self.max_length=length
		self.keycodes=keycodes
	
	def encode(self, text):
		result=""
		tmp=""
		length=0
		pair=["", ""]
		mod=0
		
		for i in range(len(text)):
			if length==self.max_length and (text[i]==" " or i==len(text)-1):
				length=0
				pair[mod%2]=f"{tmp}/"
				mod+=1
				tmp=""
			elif length==self.max_length:
				length=0
				pair[mod%2]=tmp
				mod+=1
				tmp=""
			
			if text[i]==" " and length>0:
				length=0
				pair[mod%2]=tmp
				tmp=""
				mod+=1
			elif text[i]!=" ":
				length+=1
				tmp+=text[i]
			
			if len(pair[0])!=0 and (len(pair[1])!=0 or i==len(text)-1):
				if i==len(text)-1:
					pair[mod%2]=tmp
				
				if len(result)!=0:
					result+=" "
				result+=self.keycodes[(pair[0], pair[1])]
				pair=["", ""]
		
		return result
	
	def decode(self, text):
		tmp=text.split(" ")
		result=""
		
		for i in range(len(tmp)):
			for key in self.keycodes:
				if self.keycodes[key]==tmp[i]:
					if len(key[0])==self.max_length+1:
						result+=key[0][:len(key[0])-1]
						
						if len(key[1])>0:
							result+=" "
					elif len(key[0])==self.max_length:
						result+=key[0]
					else:
						result+=key[0]
						
						if len(key[1])>0:
							result+=" "
						
					if len(key[1])==self.max_length+1:
						result+=key[1][:len(key[1])-1]
						
						if i<len(tmp)-1:
							result+=" "
					elif len(key[1])==self.max_length:
						result+=key[1]
					elif len(key[1])>0:
						result+=key[1]
						
						if i<len(tmp)-1:
							result+=" "
					
					break
		
		return result
	
	def name(self):
		return "const compression code"

